function setup() {
createCanvas(600,600);
fillColour = color(random(255), random(255), random(255));
}

function draw() {
    background(255);
    circle(300,300,400);
    fill(fillColour)
    if (keyCode = 13)
    keyPressed(UP_ARROW +- 5)
    keyPressed(LEFT_ARROW +- 5)
    keyPressed(DOWN_ARROW +- 5)
    keyPressed(RIGHT_ARROW +- 5)
}